def render_day_block(day_index: int, date_iso: str, concept_name: str,
                     concept_essay: str, comment: str) -> str:
    """1 日分の入力ブロックを整形。

    Parameters
    ----------
    day_index : 1-based の日番号（Day 1, Day 2, …）
    """
    parts = [f"--- Day {day_index} ({date_iso}) ---"]
    if concept_name:
        parts.append(f"今日の概念：{concept_name}")
    if concept_essay:
        parts.append("論考：")
        parts.append(concept_essay)
    else:
        parts.append("（論考なし）")
    if comment:
        parts.append("神山コメント：")
        parts.append(comment)
    else:
        parts.append("（コメントなし）")
    return "\n\n".join(parts)
